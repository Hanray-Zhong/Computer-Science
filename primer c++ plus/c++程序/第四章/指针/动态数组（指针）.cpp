#include <iostream>
int main()
{
    using namespace std;
    double* p3=new double [3];
    p3[0]=0.2;
    p3[1]=0.5;
    p3[2]=0.8;
    cout<<"p3[1] is "<<p3[1]<<endl;
    p3=p3+1;                         //数组序数往后推一 
    cout<<"Now p3[0] is "<<p3[0]<<" and p3[1] is "<<p3[1]<<endl;
    cout<<"p3[2] is "<<p3[2]<<"\n";                      //p3[2]变为了乱码？？？？？？？ 
    p3=p3-1;                       //需要减去一才能给delete正确的地址 
    delete [] p3;
    cin.get();
    return 0;
}
